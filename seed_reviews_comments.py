def rand_date(days_back=500):
    return datetime.now() - timedelta(
        days=random.randint(0, days_back),
        hours=random.randint(0, 23),
        minutes=random.randint(0, 59),
    )

def rand_rating_by_sentiment(sentiment):
    if sentiment == "POSITIVE":
        return random.choices(range(1, 11),
            weights=[0,0,0,1,1,3,5,10,14,16], k=1)[0]
    elif sentiment == "NEGATIVE":
        return random.choices(range(1, 11),
            weights=[8,10,8,6,4,3,2,1,0,0], k=1)[0]
    else:
        return random.choices(range(1, 11),
            weights=[1,1,2,3,5,8,8,5,3,1], k=1)[0]

